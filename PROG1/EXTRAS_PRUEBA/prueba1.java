package PROG1.EXTRAS_PRUEBA;

public class prueba1 {
    public static void main(String[] args) {
        String palabra = "hola";
        switch (palabra) {
            case "chau":
                System.out.println("la palabra es chau");
                break;
            case "hola":
                System.out.println("la palabra es hola");
                break;
            default:
                System.out.println("no es una palabra");
                break;
        }
    }

}
